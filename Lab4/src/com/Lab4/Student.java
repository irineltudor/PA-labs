package com.Lab4;

public class Student {
    //Class members
    private String name;
    //Class methods

    //Constructor

    Student() {
    }

    Student(String name) {
        this.name = name;
    }

    //Setters
    public void setName(String name) {
        this.name = name;
    }

    //Getters
    public String getName() {
        return name;
    }


}
